"""抖音短视频脚本 Prompt 模板"""

SYSTEM = """你是抖音平台的顶级带货短视频编导，精通抖音算法和用户心理。
你的视频脚本完播率始终高于行业均值50%以上。"""


def build_prompt(product_info: str, style: str = "种草", notes: str = "") -> str:
    return f"""请为以下产品生成一条抖音短视频带货脚本。

## 产品信息
{product_info}

## 风格：{style}
{f"## 额外要求：{notes}" if notes else ""}

## 脚本规范
1. 总时长 30-60 秒，9:16 竖屏
2. 必须包含以下结构：
   - 0-3秒【Hook/钩子】：制造冲突、悬念或共鸣，让人停下拇指
     好的Hook举例："穿了3年瑜伽裤，终于找到一条不尴尬的！"
     差的Hook举例："今天给大家推荐一款瑜伽裤"
   - 3-10秒【痛点放大】：说出目标用户的真实烦恼
   - 10-30秒【产品展示】：必须有 before/after 对比或上身效果，纯口播禁止
   - 30-45秒【信任背书】：工厂直发/退货包运费/销量数据
   - 45-60秒【逼单】：限时价格 + 行动号召

3. 违禁词检查：不能出现"最好/第一/绝对/100%/国家级"
4. 必须推荐适合的BGM风格（不指定具体歌曲名）

## 输出JSON格式
{{
  "title": "视频标题（用于抖音发布）",
  "hook": "开头3秒的文案",
  "scenes": [
    {{
      "scene_number": 1,
      "duration": "0-3秒",
      "shot_type": "镜头类型（特写/中景/全景）",
      "visual": "画面描述",
      "voiceover": "口播文案",
      "text_overlay": "画面文字（如有）"
    }}
  ],
  "bgm_style": "BGM风格建议",
  "hashtags": ["#标签1", "#标签2"],
  "publishing_tips": {{
    "best_time": "最佳发布时间",
    "cover_suggestion": "封面建议"
  }}
}}

只输出JSON。"""
