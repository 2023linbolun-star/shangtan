"""拼多多商品文案 Prompt 模板"""

SYSTEM = """你是拼多多平台的资深运营，精通拼多多的价格敏感用户心理和平台规则。
你知道PDD用户决策快、看详情少、对价格极度敏感。"""


def build_prompt(product_info: str, style: str = "性价比", notes: str = "") -> str:
    return f"""请为以下产品生成拼多多商品文案。

## 产品信息
{product_info}

## 风格：{style}
{f"## 额外要求：{notes}" if notes else ""}

## 拼多多文案规范（与淘宝的关键差异）

1. 主图要求：
   - 强调"价格感"：券后价/拼团价必须突出
   - 白底图为主
   - 主图上可以加促销信息（和淘宝不同）

2. 详情页要求：
   - 更短更直接（PDD用户不看长详情）
   - 前3屏必须打动用户（核心卖点+价格优势+销量/好评）
   - 严禁出现"天猫同款"、"淘宝爆款"等引流词
   - 突出"工厂直发"、"源头好货"等PDD调性词

3. 标题要求（60字符）：
   - 品类词+属性堆叠+信任词
   - 加"包邮"、"正品"等信任词
   - 不加品牌名（除非是旗舰店）
   - 比淘宝更侧重属性堆叠（PDD搜索靠关键词覆盖）

## 输出JSON格式
{{
  "title": "拼多多优化标题（60字符内）",
  "main_images": [
    {{"position": 1, "description": "图片描述", "price_display": "价格展示方式"}}
  ],
  "selling_points_short": ["核心卖点1（一句话）", "核心卖点2", "核心卖点3"],
  "detail_sections": [
    {{"title": "段落标题", "content": "简短文案", "image_suggestion": "配图建议"}}
  ],
  "seo_keywords": ["关键词1", "关键词2", ...],
  "price_strategy": "定价建议（参考竞品最低价）"
}}

只输出JSON。"""
