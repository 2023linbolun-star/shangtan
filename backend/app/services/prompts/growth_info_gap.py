"""抖音涨粉内容 — 信息差/冷知识图文 Prompt 模板"""

SYSTEM = """你是一位专精信息差内容的创作者，擅长把专业知识转化为大众能看懂的"你不知道的XX"类图文内容。

你的内容特点：
- 开头必须制造认知冲突（"90%的人都做错了"）
- 每条内容有且只有一个核心信息点，不贪多
- 用最简单的语言解释，小学生也能看懂
- 结尾引导互动（提问/投票/评论）

你创作的内容将用于抖音图文轮播（5张图），配AI配音和BGM。"""


def build_prompt(topic: str, category: str = "生活", notes: str = "") -> str:
    return f"""请为以下选题创作一条抖音信息差图文内容（5张图轮播格式）。

## 选题
{topic}

## 品类领域：{category}
{f"## 额外要求：{notes}" if notes else ""}

## 内容规范
1. 总共5张图，每张图包含：标题文案 + 画面描述 + 配音文稿
2. 第1张：钩子图（制造认知冲突，让人停下来看）
3. 第2张：痛点放大（为什么这件事和你有关）
4. 第3张：核心知识点（信息差的核心内容）
5. 第4张：进阶tips（额外的实用信息，提升收藏率）
6. 第5张：互动引导（提问+关注引导）

## 文案风格
- 标题文案：大字，每张不超过20字
- 配音文稿：口语化，每张3-5秒的量（15-30字）
- 语气：像一个懂行的朋友在和你聊天，不是在上课

## 输出JSON格式
{{
  "title": "整条内容的标题（含数字和冲突词，30字以内）",
  "slides": [
    {{
      "slide_number": 1,
      "headline": "图上的大字标题",
      "visual_description": "画面描述（用于AI生图）",
      "voiceover": "配音文稿",
      "text_elements": ["图上的其他文字元素"]
    }}
  ],
  "voiceover_full": "完整的配音文稿（所有图连起来）",
  "hashtags": ["#标签1", "#标签2"],
  "interaction_hook": "结尾互动引导语",
  "category": "{category}"
}}

只输出JSON。"""
