"""淘宝/天猫商品文案 Prompt 模板"""

SYSTEM = """你是淘宝/天猫的资深运营文案，精通搜索电商的商品详情页设计。
你深谙消费者在淘宝的浏览习惯：先看主图决定点击，再看详情页决定购买。"""


def build_prompt(product_info: str, style: str = "专业", notes: str = "") -> str:
    return f"""请为以下产品生成淘宝/天猫商品文案。

## 产品信息
{product_info}

## 风格：{style}
{f"## 额外要求：{notes}" if notes else ""}

## 文案规范

### 主图描述（5张）
1. 第一张（搜索主图）：白底或浅色背景，商品占比>60%，不允许牛皮癣文字
2. 第二张：核心卖点图（1个最打动人的卖点）
3. 第三张：使用场景/模特效果图
4. 第四张：面料/工艺/细节特写
5. 第五张：尺码表或参数对比

### 核心卖点（不超过5个）
每个卖点用"痛点→方案"结构：
  示例："蹲下弯腰总尴尬？→ 4D立体裁剪，怎么动都不透"

### 详情页结构
1. 模特上身效果（3-5个场景）
2. 核心卖点展开（配图+文案）
3. 面料/工艺细节特写
4. 精确尺码表（减少退货的关键）
5. 实物细节图
6. 品牌/工厂/资质背书
7. 售后承诺

### SEO关键词
提取适合放在淘宝后台"关键属性"中的搜索词

## 输出JSON格式
{{
  "main_images": [
    {{"position": 1, "description": "图片内容描述", "text_overlay": "图上文字（如有）"}}
  ],
  "selling_points": [
    {{"pain_point": "用户痛点", "solution": "产品方案", "proof": "证据/数据"}}
  ],
  "detail_sections": [
    {{"title": "段落标题", "content": "文案内容", "image_suggestion": "配图建议"}}
  ],
  "size_chart_notes": "尺码表建议说明",
  "seo_keywords": ["关键词1", "关键词2", ...],
  "after_sales": "售后承诺文案"
}}

只输出JSON。"""
